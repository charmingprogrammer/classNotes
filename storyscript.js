
      function on() {
        document.querySelector("*").style.backgroundColor = "black";
        document.querySelector("*").style.color = "gray";
        document.querySelector("#header").style.backgroundColor = "black";
        document.querySelector("#header").style.color = "gray";
        
      }
      function off() {
        document.querySelector("*").style.backgroundColor = "";
        document.querySelector("*").style.color = "";
        document.querySelector("#header").style.backgroundColor = "";
        document.querySelector("#header").style.color = "";
        
      }
    
  